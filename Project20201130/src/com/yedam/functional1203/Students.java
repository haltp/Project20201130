package com.yedam.functional1203;

public class Students {
	private String name;
	private String sex;
	private int score;

	public Students(String name, String sex, int score) {
		this.name = name;
		this.sex = sex;
		this.score = score;
	}

	public String getName() {
		return name;
	}

	public String getSex() {
		return sex;
	}

	public int getScore() {
		return score;
	}


}
